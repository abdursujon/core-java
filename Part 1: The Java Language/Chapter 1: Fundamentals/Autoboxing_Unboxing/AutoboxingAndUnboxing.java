package Autoboxing_Unboxing;

/**
 * Autoboxing is the automatic conversion of a primitive type to its corresponding wrapper class (e.g., int -> Integer)
 * Unboxing is the reverse of Autoboxing (e.g., Integer -> int)
 */
public class AutoboxingAndUnboxing {
    int num = 1242322;
    Integer autoboxing = num;

    Integer num2 = 34555;
    int unboxing = num2;
}
